# –§—É–Ω–∫—Ü–∏—è –≤—ã–±–æ—Ä–∞ —Å–ª—É—á–∞–π–Ω–æ–π —Ü–∏—Ç–∞—Ç—ã
def get_random_quote():
    return f"üíå *{random.choice(QUOTES)}*"

# –û—Ç–ø—Ä–∞–≤–∫–∞ —Ü–∏—Ç–∞—Ç—ã –≤ –∫–∞–Ω–∞–ª
async def send_quote_to_channel():
    app = ApplicationBuilder().token(TOKEN).build()
    quote = get_random_quote()
    await app.bot.send_message(chat_id=CHANNEL_ID, text=quote, parse_mode="Markdown")
    logging.info("–û—Ç–ø—Ä–∞–≤–ª–µ–Ω–∞ —Ü–∏—Ç–∞—Ç–∞ –≤ –∫–∞–Ω–∞–ª: %s", quote)

# /start
async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(get_random_quote(), parse_mode="Markdown")

# –ì–ª–∞–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è
async def main():
    app = ApplicationBuilder().token(TOKEN).build()

    # –ö–æ–º–∞–Ω–¥—ã
    app.add_handler(CommandHandler("start", start))

    # –ü–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫
    scheduler = AsyncIOScheduler(timezone="Asia/Tashkent")
    scheduler.add_job(send_quote_to_channel, "cron", hour=10, minute=0)
    scheduler.start()

    if MODE == "webhook":
        await app.run_webhook(
            listen="0.0.0.0",
            port=PORT,
            url_path=TOKEN,
            webhook_url=f"{WEBHOOK_BASE}/{TOKEN}"
        )
    else:
        await app.run_polling()

if __name__ == "__main__":
    import asyncio
    asyncio.run(main())