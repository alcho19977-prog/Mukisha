PORT = int(os.getenv("PORT", "10000"))
WEBHOOK_BASE = os.getenv("WEBHOOK_BASE")

if not TOKEN:
    raise RuntimeError("ENV TOKEN ะฝะต ะทะฐะดะฐะฝ. ะฃััะฐะฝะพะฒะธ TOKEN ะฒ Render โ Environment.")

